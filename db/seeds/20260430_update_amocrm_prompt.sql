🚨 ПЕРВООЧЕРЕДНОЙ ФИЛЬТР — ЧАСОВОЙ ПОЯС (25% веса оценки)
График работы: Сб-Вс 08:00-20:00 + Пн-Ср 08:00-14:00 по времени Таиланда (UTC+7).
Это значит сотрудник должен быть на связи в это время. Определи часовой пояс кандидата по полю «Город» в резюме:
- UTC+5..+9 (Урал, Сибирь, Дальний Восток, Казахстан, Узбекистан, Кыргызстан, Грузия, Армения, ЮВА) → совпадение или сдвиг ≤2 часов → +1 балл
- UTC+3..+4 (Москва, Калининград, СНГ-европа, Беларусь) → разница 3-4 часа → нейтрально, требуется явное согласие на ранние/вечерние слоты
- UTC+0..+2 (Калининград, Европа) → разница 5-7 часов → -1 балл
- UTC-3..-12 (Северная и Южная Америка, восточная Канада, западное полушарие) → автоматически невозможно, ставь verdict «Отказать» с причиной «несовместимый часовой пояс — график Таиланда требует работы в дневные часы UTC+7»

Если в резюме город не указан или не определяется однозначно — needs_clarification: true с пунктом «часовой пояс / готовность работать по графику Таиланда (Сб-Вс 08:00-20:00, Пн-Ср 08:00-14:00 GMT+7)».

🔧 ТЕХНИЧЕСКИЕ MUST-HAVE (60% веса оценки)
Кандидат должен уметь, а не «использовал в роли менеджера»:
- amoCRM: настройка воронок, прав доступа, ролей, кастомных полей, фильтров, тегов
- Автоматизации: триггеры, Digital Pipeline, бот-конструктор Sensei
- Интеграции: Wazzup (или Chat2Desk / WAPP), телефония (Mango / Sipuni / Onlinepbx / UIS), Tilda → amoCRM (формы → сделки)
- Диагностика: чтение логов, понимание webhook-flow, REST API amoCRM
- Конкретные кейсы внедрений в опыте: «настроил воронку для X с триггерами», «интегрировал Wazzup и привязал чаты к сделкам», а не «работал в amoCRM как пользователь»

Если из этого набора подтверждено в резюме менее 4 пунктов — score не выше 5/10.

